package SimpleCompilerPackage;

public class ExecutionException extends RuntimeException {

	private static final long serialVersionUID = -3324122465449704562L;

	public ExecutionException() {
		super();
	}

	public ExecutionException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ExecutionException(String arg0) {
		super(arg0);
	}

	public ExecutionException(Throwable arg0) {
		super(arg0);
	}

	
	
}
